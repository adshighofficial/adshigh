export function keyFromSlug(slug) {
  if (!slug) return null;
  const s = String(slug).toLowerCase();
  let found = null;
  Object.entries(SLUG_MAP).some(([key, arr]) => {
    if (arr.includes(s)) { found = key; return true; }
    return false;
  });
  return found;
}

